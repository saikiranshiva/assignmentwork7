class Car:
    def __init__(self,model,brand,color):
        self.model=model
        self.brand=brand
        self.color=color
    def start(self):
        print("car is staring")
    def move(self):
        print("car is moving")
    def stop(self):
        print("car is stoping")
class BMW(Car):
    def __init__(self,model,brand,color):
        super().__init__(model,brand,color)

    def autopilot(self):
        print("autopilot")
    def autogear(self):
        print("autogear")
    def bmwfeatures(self):
        print("bmw model:",self.model)
        print("bmw brand:",self.brand)
        print("bmw color:",self.color)
        
